package day05_practice_task;

import javax.swing.*;

public class Rectangle {
    public static void main(String[] args) {
        int num = 10;
        for (int row = 1; row < num; row++) {
            for (int column = 1; column < num; column++) {
                System.out.print(" * ");

            }
            System.out.println();
        }
    }
}


















